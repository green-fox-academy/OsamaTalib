package com.company;

public class TwoNumbers {
  public static void main(String[] args) {
    int a = 22;
    int b =13;
    double c = a/b;
    System.out.println("the result of 13 added to 22 is = " + (a+b));
    System.out.println("the result of 13 substracted from 22 is = " + (a-b));
    System.out.println("the result of 22 multiplied by 13 is = " + (a*b));
    System.out.println("the result of 22 divided by 13 (as a decimal fraction) is = " + c);
    System.out.println("the integer part of 22 divided by 13 is = " + (a/b));
    System.out.println("the reminder of 22 divided by 13 = " + (a % b));
  }
}
